package design.pattern.builder;

public class Product {

	private String partA;
	
	private String partB;
	
	private String partC;

	public String getPartsA() {
		return partA;
	}

	public void setPartsA(String partA) {
		this.partA = partA;
	}

	public String getPartsB() {
		return partB;
	}

	public void setPartsB(String partB) {
		this.partB = partB;
	}

	public String getPartsC() {
		return partC;
	}

	public void setPartsC(String partC) {
		this.partC = partC;
	}
	
}
